// FILE STATUS IN GIT:-
// 1. Untracked: - files that are not being tracked by git. They are new files that have been added to
//  the directory but have not been staged or committed yet.
// 2. Staged: - files that have been added to the staging area and are ready to be committed.
// 3. Committed: - files that have been committed to the repository and are part of the project history.
// 4. Modified: - files that have been changed but have not been staged or committed yet.
// 5. Deleted: - files that have been removed from the directory but have not been staged or committed yet.
// 6. unmodified: - files that have not been changed since the last commit.
//  They are the same as the last committed version of the file.
// 7. Renamed: - files that have been renamed but have not been staged or committed yet.


